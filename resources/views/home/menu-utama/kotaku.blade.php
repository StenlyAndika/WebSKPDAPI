    <p style="font-size: 14px; text-align: left;">
    Implementasi pencegahan dan peningkatan kualitas permukiman kumuh, dimulai dari tahap 
    (a) pendataan; (b) perencanaan; (c) pelaksanaan, (d) pemantauan dan evaluasi dan (e) keberlanjutan. 
    Setiap tahapan dilakukan secara partisipatif dengan melibatkan masyarakat (LKM/BKM), 
    pemerintah kabupaten/kota dan pemangku kepentingan lainnya (stakeholder). 
    Disadari bahwa kegiatan pencegahan dan peningkatan kualitas permukiman kumuh berkaitan erat 
    dengan masyarakat dan sebagai implementasi dari prinsip bahwa pembangunan yang dilakukan 
    (termasuk pencegahan dan peningkatan kualitas permukiman kumuh) tidak boleh merugikan masyarakat, 
    maka dalam pelaksanaan Program Kotaku selalu menerapkan penapisan (pengamanan) lingkungan 
    dan sosial (environment and social safeguard).
    <br><br>
    Sumber pembiayaan Program Kotaku berasal dari pemerintah pusat, pemerintah provinsi, pemerintah kabupaten/kota, swadaya masyarakat dan pemangku kepentingan lainya (stakeholder) serta dari lembaga mitra pembangunan pemerintah (World Bank-WB; Asian Infrastructure Investment Bank-AIIB dan Islamic Development Bank-IsDB). Berdasarkan kebutuhan total pembiayaan, sumber dari mitra pembangunan pemerintah (Loan) sekitar 45%.<br><br>

    Tujuan umum program ini adalah meningkatkan akses terhadap infrastruktur dan pelayanan dasar di permukiman kumuh perkotaan dan mencegah timbulnya permukiman kumuh baru dalam rangka untuk mendukung terwujudnya permukiman perkotaan yang layak huni, produktif, dan berkelanjutan. <br>
    Untuk mewujudkan tujuan diatas, dilakukan melalui kegiatan:
    <br>(a) Pembangunan/rehabilitasi infrastruktur permukiman baik skala lingkungan maupun skala kawasan;
    <br>(b) Penguatan kapasitas masyarakat dan pemerintah daerah serta
    <br>(c) Pembangunan infrastruktur pendukung penghidupan (livelihood) masyarakat.
    <br>
    <br>Sesuai dengan Permen PUPR No. 14 tahun 2018 Tentang Pencegahan dan Peningkatan Kualitas Perumahan Kumuh dan Permukiman Kumuh, yang terdiri dari 7 aspek dan 16 kriteria permukiman kumuh adalah sebagai berikut:
    <br>
    <br><strong>1. Kondisi Bangunan Gedung</strong>
    <br>    • Ketidakteraturan bangunan;
    <br>    • Tingkat kepadatan bangunan yang tinggi yang tidak sesuai dengan ketentuan rencana tata ruang; dan/atau
    <br>    • Kualitas bangunan yang tidak memenuhi syarat
    <br><strong>2. Kondisi Jalan Lingkungan</strong>
    <br>    • Jaringan jalan lingkungan tidak melayani seluruh lingkungan Perumahan atau Permukiman; dan/atau
    <br>    • Kualitas permukaan jalan lingkungan buruk.
    <br><strong>3. Kondisi Penyediaan Air Minum</strong>
    <br>    • Akses aman air minum tidak tersedia; dan/atau
    <br>    • Kebutuhan air minum minimal setiap individu tidak terpenuhi
    <br><strong>4. Kondisi Drainase Lingkungan</strong>
    <br>    • Drainase lingkungan tidak tersedia;
    <br>    • Drainase lingkungan tidak mampu mengalirkan limpasan air hujan sehingga menimbulkan genangan; dan/atau
    <br>    • Kualitas konstruksi drainase lingkungan buruk
    <br><strong>5. Kondisi Pengelolaan Air Limbah</strong>
    <br>    • Sistem pengelolaan air limbah tidak memenuhi persyaratan teknis; dan/atau
    <br>    • Prasarana dan sarana pengelolaan air limbah tidak memenuhi persyaratan teknis
    <br><strong>6. Kondisi Pengelolaan Persampahan</strong>
    <br>    • Prasarana dan sarana persampahan tidak memenuhi persyaratan teknis; dan/atau
    <br>    • Sistem pengelolaan persampahan tidak memenuhi persyaratan teknis.
    <br>    • Kondisi Pengamanan (Proteksi) Kebakaran
    <br>    • Prasarana proteksi kebakaran tidak tersedia; dan
    <br>    • Sarana proteksi kebakaran tidak tersedia
    <br>
    <br>Dan sebagai aspek tambahan, yaitu Ketersediaan Ruang Terbuka Publik

    Program Kotaku dilaksanakan di 34 provinsi, yang tersebar di 269 kabupaten/kota, pada 11.067 desa/kelurahan. Berdasarkan Surat Keputusan (SK) Kumuh yang ditetapkan oleh kepala daerah masing-masing kabupaten/kota, permukiman kumuh yang berada di lokasi sasaran Program Kotaku adalah seluas 23.656 Hektare.

    Kotaku di Kota Sungai Penuh telah hadir dari Tahun 2015-2022 mendampingi 19 desa kelurahan di 3 Kecamatan yang telah dibiayai oleh Program Kotaku melalui pembiayaan skala lingkungan (BPM Reguler, Livelihood dan Cash for Work)	

