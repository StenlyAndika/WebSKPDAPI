                        <p style="font-size: 14px; text-align: left;">
    Program Kota Tanpa Kumuh (Kotaku) merupakan salah satu upaya strategis Direktorat Jenderal Cipta Karya Kementerian Pekerjaan Umum dan Perumahan Rakyat
    untuk mempercepat penanganan permukiman kumuh di perkotaan dan mendukung
    <strong>“Gerakan 100-0-100”</strong>, yaitu <strong>100 persen akses air minum layak, 0 persen permukiman kumuh, dan 100 persen akses sanitasi layak</strong>. Program Kotaku dalam pelaksanaannya menggunakan platform kolaborasi antara pemerintah pusat, pemerintah provinsi, kota/kabupaten, masyarakat dan stakeholder lainya dengan memposisikan masyarakat dan pemerintah kabupaten/kota sebagai pelaku utama (nakhoda).<br><br>
